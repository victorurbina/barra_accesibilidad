<h1>Información para los usuarios con discapacidad</h1>

<p>Tanto las opciones de accesibilidad que brindamos como la compatibilidad con 
   lectores de pantalla, hacen que la página que usted está por navegar sea fácil
   de usar por personas afectadas por discapacidades sensoriales o motoras.</p>

<p>En el caso de discapacidad visual, la navegación por Internet se ve muy beneficiada
   si se realiza con la ayuda técnica de un programa (software) llamado "Lector de
   Pantalla", que debe estar instalado y configurado en el equipo.</p>

<p>Recomendamos el uso del lector de pantalla <strong>NVDA</strong> que es de código
   abierto, de uso gratuito, y tiene una gran variedad de funcionalidades. Se puede
   descargar desde su sitio web: <a href="http://www.nvaccess.org/download" target=
   "_blank">"http://www.nvaccess.org/download"</a></p>

<h2>Navegación con un Lector de Pantallas.</h2>
<ol>
    <li>Para facilitar la navegación, hemos distribuído el contenido del sitio en diferentes
        regiones (según las directrices de la W3C) que permiten acceder a la información
        deseada con sólo pulsar una tecla prestablecida (definida en el Lector de Pantalla).</li>

    <li>Todas las páginas permiten a los usuarios con discapacidad, navegar de forma
        libre e intuitiva, utilizando el teclado con el Lector de Pantallas. Para una
        mejor experiencia, animamos a familiarizarse con las combinaciones de teclas
        del Lector de Pantalla para encontrar documentos, completar formularios o acceder
        de manera directa a los menúes y botones.</li>

    <li>Todas las páginas ofrecen la posibilidad de saltar los contextos repetitivos
        para ir directo al contenido buscado. Sugerimos utilizarlos para agilizar la
        navegación. </li>

    <li>Cada página ofrece un título que, mediante la correspondiente combinación de
        teclas, ayudará a los usuarios de ayudas técnicas a responder la pregunta dónde
        estoy.</li>

    <li>En caso de no disponer de lector de pantalla, a continuación ofrecemos un enlace
        directo para su descarga (sugerimos incluir el sintetizador de voz optimizado).</li>
</ol>

