<h1>¿Qué es la Accesibilidad Web?</h1>

<p>Es la posibilidad de que un Sitio Web pueda ser visitado y utilizado de forma
   satisfactoria por todas las personas, teniendo en cuenta y brindando facilidades
   a quienes tienen movilidad y/o comunicación reducida.</p>

<p>Es poder percibir, entender, navegar e interactuar con la web independientemente
   de las condiciones particulares de cada persona. </p>

<h1>¿Quién define los parámetros de la Accesibilidad Web?</h1>

<p>La <strong>Disposición Nº 2/2014</strong> de la <strong>Oficina Nacional de
   Tecnologías de la Información</strong> establece los requisitos de accesibilidad
   para contenidos web basándose en las <strong>Pautas de Accesibilidad para el
   Contenido Web</strong> (WCAG, Web Content Accesibility Guidelines) en su versión
   2.0 publicadas en el año 2008 por uno de los grupos de trabajo de la <strong>
   "Iniciativa para la Accesibilidad Web"</strong> (WAI, Web Accesibility Initiative)
   del <strong>Consorcio de la Web</strong> (W3C, Word Wide Web Consortium). Además,
   adopta el estándar internacional ISO/IEC 40500:2012 que aprueba las pautas de
   accesibilidad web WCAG 2.0.</p>

